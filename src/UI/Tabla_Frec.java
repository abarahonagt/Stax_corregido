    private void borrarFilas() {
        DefaultTableModel modelo = (DefaultTableModel) tb_tabla.getModel();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(0);
        }
    }
    
    private float redondear(float d, int i) {
        float valor = 0;
        int factor=10*(i+1);
        valor = d;
        valor = valor * factor;
        valor = java.lang.Math.round(valor);
        valor = valor / factor;

        return valor;
    }
   
